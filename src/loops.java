public class loops {
    public static void main(String[] args) {
        for (int i = 0; i < 9; i++) {
            System.out.print(i + ", ");
        }
        for (int i = 7; i < 13; i++) {
            System.out.print(i + ", ");
        }
        for (int i = 12; i < 31; i += 3) {
            System.out.print(i + ", ");
        }
        for (int i = 100; i >= 0 ; i -= 2) {
            System.out.print(i + ", ");
        }
    }
}
