public class Rand {
    public static void main(String[] args) {

        System.out.println(Math.random());
        System.out.println(Math.random() * 100.0);
        System.out.println(Math.random() + 5.0);
        System.out.println(Math.random() * 20.0 - 10.0);
        System.out.println(Math.random() * 52 + 1);

    }
}
